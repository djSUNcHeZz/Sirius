'''
–¢—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ –ü–∞—Å–∫–∞–ª—è

–î–∞–Ω—ã –¥–≤–∞ —á–∏—Å–ª–∞ n m
–°–æ–∑–¥–∞–π—Ç–µ –º–∞—Å—Å–∏–≤ ùëõ√óùëö –∏ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ –µ–≥–æ –ø–æ —Å–ª–µ–¥—É—é—â–∏–º –ø—Ä–∞–≤–∏–ª–∞–º:
–ß–∏—Å–ª–∞, —Å—Ç–æ—è—â–∏–µ –≤ —Å—Ç—Ä–æ–∫–µ 0 –∏–ª–∏ –≤ —Å—Ç–æ–ª–±—Ü–µ 0 —Ä–∞–≤–Ω—ã 1 (A[0][j] = 1, A[i][0] = 1).
–î–ª—è –≤—Å–µ—Ö –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Å—Å–∏–≤–∞ A[i][j] = A[i-1][j] + A[i][j-1],
—Ç–æ –µ—Å—Ç—å –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç —Ä–∞–≤–µ–Ω —Å—É–º–º–µ –¥–≤—É—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤, —Å—Ç–æ—è—â–∏—Ö —Å–ª–µ–≤–∞ –∏ —Å–≤–µ—Ä—Ö—É –æ—Ç –Ω–µ–≥–æ.
–í—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
–í–≤–æ–¥—è—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ n m, –Ω–µ –ø—Ä–µ–≤—ã—à–∞—é—â–∏—Ö 100
'''
n, m = map(int, input().split())
A = [[0]*m for i in range(n)]
for i in range(n):
	for j in range(m):
		if i == 0:
			A[i][j] = 1
		elif j == 0:
			A[i][j] = 1
		else:
			A[i][j] = A[i-1][j] + A[i][j-1]
for s in A:
	print(*s)
