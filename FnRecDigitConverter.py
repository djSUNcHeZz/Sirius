def digit_convert(P, X):
	'''–ø–µ—Ä–µ–≤–æ–¥–∏—Ç —á–∏—Å–ª–æ X –≤ –†-–∏—á–Ω—É—é —Å–∏—Å—Ç–µ–º—É —Å—á–∏—Å–ª–µ–Ω–∏—è'''
	global result
	if X > 1:
		digit_convert(P, X//P)
	result += str(X%P)
	return result
		
		
result, P, X = '', input(), input()
print(X+'(10)=' + digit_convert(int(P), int(X)).lstrip('0') + '('+P+')')
