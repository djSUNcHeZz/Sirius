	if res > 0: #–≤—ã—Ö–æ–¥–∏–º –¥–ª—è –≤—ã–≤–æ–¥–∞ –Ω–∞–∏–º–µ–Ω—å—à–µ–≥–æ —Ä—è–¥–∞
		break
