'''
–ó–Ω–∞—á–µ–Ω–∏–µ –≤—ã—Ä–∞–∂–µ–Ω–∏—è
–î–∞–Ω–∞ —Å—Ç—Ä–æ–∫–∞, —Å–æ—Å—Ç–æ—è—â–∞—è –∏–∑ ùëõ —Ü–∏—Ñ—Ä (—Ç.–µ. –æ–¥–Ω–æ–∑–Ω–∞—á–Ω—ã—Ö —á–∏—Å–µ–ª),
–º–µ–∂–¥—É –∫–æ—Ç–æ—Ä—ã–º–∏ —Å—Ç–æ–∏—Ç ùëõ‚àí1
–∑–Ω–∞–∫ –æ–ø–µ—Ä–∞—Ü–∏–∏, –∫–∞–∂–¥—ã–π –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö –º–æ–∂–µ—Ç –±—ã—Ç—å –ª–∏–±–æ +, –ª–∏–±–æ ‚àí
–í—ã—á–∏—Å–ª–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–∞–Ω–Ω–æ–≥–æ –≤—ã—Ä–∞–∂–µ–Ω–∏—è.
'''
S = input()
result = int(S[0])
for i in range(1, len(S)-1, 2):
	if S[i] == '+':
		result += int(S[i+1])
	else:
		result -= int(S[i+1])
print(result)
	
