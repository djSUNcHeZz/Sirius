N = int(input())
S = [int(i) for i in input().split()]
res = 2**31
n, m = 0, 0
for n in range(N-1):
	for m in range(n+1, N):
		dif = abs(S[n]-S[m])
		if dif < res:
			res, res_n, res_m = dif, n, m
print(res_n, res_m)
