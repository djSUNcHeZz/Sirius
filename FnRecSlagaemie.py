def count_slag(n, k):
	'''–≤—ã–≤–æ–¥–∏—Ç k-–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ä–∞–∑–±–∏–µ–Ω–∏–π –Ω–∞ —Å–ª–∞–≥–∞–µ–º—ã–µ —á–∏—Å–ª–∞ n'''
	if n == 0:
		return 1
	res = 0
	for i in range(1, min(n, k)+1):
		res += count_slag(n-i, i)
	return res


n = int(input())	
print(count_slag(n, n))
